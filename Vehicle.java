package com.company;

public class Vehicle {
    String vehicleName;
    String vehicleColor;
    int year;
    int price;

    public Vehicle(String vehicleName, String vehicleColor, int year, int price) {
        this.vehicleName = vehicleName;
        this.vehicleColor = vehicleColor;
        this.year = year;
        this.price = price;
    }


}
